package main.java.model;

public class TypeLocality {
    private String tnp;
    private String name;

    public TypeLocality(String tnp, String name) {
        this.tnp = tnp;
        this.name = name;
    }

    public String getTnp() {
        return tnp;
    }

    public String getName() {
        return name;
    }
}

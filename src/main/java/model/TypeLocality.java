package main.java.model;

public class TypeLocality {
    private String rgn;
    private String name;

    public TypeLocality(String rgn, String name) {
        this.rgn = rgn;
        this.name = name;
    }

    public String getRgn() {
        return rgn;
    }

    public String getName() {
        return name;
    }
}
